#include <stdio.h>

/**
 * main - code
 *
 * Return: Always 0.
 */
int main(void)
{
	int i;

	for (i = 1; i <= 100; i++)
	{
		if ((i % 3 == 0) && (i % 5 == 0))
		{
			printf("FizzBuzz ");
		}
		else if (i % 5 == 0)
		{
			printf("Buzz ");
		}
		else if (i % 3 == 0)
		{
			printf("fizz ");
		}
		else
		{
			printf("%i ", i);
		}
	}
	printf("\n");
	return (0);
}
